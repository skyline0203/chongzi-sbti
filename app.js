// ============================================================
// app.js — 小程序总开关
// ============================================================
// 
// 这个文件是小程序的"入口"，就像 C 语言的 main() 函数。
// 小程序启动时，这个文件最先执行。
// 所有页面共享这里定义的全局数据。
//
// App() 是微信提供的函数，用来创建一个小程序实例。
// 你只能有一个 App()，多写会报错。
// ============================================================

App({
  // ----------------------------------------------------------
  // onLaunch — 小程序启动时自动执行的函数
  // 类比：C 语言程序启动时 main() 里的初始化代码
  // ----------------------------------------------------------
  onLaunch() {
    console.log('艾斯BTI 职业精神鉴定 启动！');

    // --------------------------------------------------------
    // 检查本地是否有登录缓存
    // wx.getStorageSync = 从本地存储读取数据（同步方式）
    // 如果之前登录过，这里会读到之前保存的用户信息
    // --------------------------------------------------------
    const isLoggedIn = wx.getStorageSync('isLoggedIn');
    if (isLoggedIn) {
      console.log('用户已登录，跳过登录流程');
    }
  },

  // ----------------------------------------------------------
  // globalData — 全局数据
  // 所有页面都可以通过 getApp().globalData 访问这些数据
  // 类比：C 语言中的全局变量
  // ----------------------------------------------------------
  globalData: {
    // 广告位 ID（需要替换成你在微信广告平台申请的真实 ID）
    // 格式：'adunit-xxxxxxxxxxxxxxxx'
    adUnitId: 'adunit-xxxxxxxxxxxxxxxx',

    // CDN 基础路径（预留，后续版本可以用来加载远程资源）
    cdnBase: '',

    // 服务端 API 地址（后续接入后端时替换）
    // 目前是空的，用本地模拟
    apiBase: ''
  }
})
